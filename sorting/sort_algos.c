int insertion_sort(int * arr, int n) {
	int i,j,temp;
	if ( n==0 || n==1) return 0;

	for ( i = 1; i < n-1; i++) {
		j = i-1;
		while (j > -1 && arr[j]>arr[j+1]) {
			temp = arr[j+1];
			arr[j+1] = arr[j];
			arr[j] = temp;
			j--;
		}
	}
	return 0;
}

